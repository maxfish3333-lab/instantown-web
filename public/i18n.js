// Funzione globale per ottenere traduzione
function i18n(key) {
  const lang = localStorage.getItem('it_lang') || 'it';
  return (IT18N[lang] || IT18N.it)[key] || key;
}

// Applica lingua a tutti gli elementi data-i18n
function applyLang(lang) {
  localStorage.setItem('it_lang', lang);
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = (IT18N[lang] || IT18N.it)[key];
    if(val !== undefined) el.innerHTML = val;
  });
  // Aggiorna selettori lingua
  document.querySelectorAll('.lang-sel, #lang-sel').forEach(sel => {
    sel.value = lang;
  });
  document.querySelectorAll('.lbtn').forEach(b => {
    b.classList.toggle('active', b.getAttribute('data-lang') === lang);
  });
}

// ── RILEVA LINGUA SISTEMA ──
function rilevaLingua() {
  // 1. Usa lingua salvata dall'utente se già scelta
  const saved = localStorage.getItem('it_lang');
  if(saved) return saved;

  // 2. Rileva lingua del browser/sistema (Android/iOS)
  const sys = (navigator.language || navigator.userLanguage || 'it').toLowerCase();

  // Mappa codici lingua → lingue supportate
  if(sys.startsWith('zh')) return 'zh';
  if(sys.startsWith('es')) return 'es';
  if(sys.startsWith('fr')) return 'fr';
  if(sys.startsWith('en')) return 'en';
  // Default italiano per tutti gli altri (it, de, pt, ar, ja, ko...)
  return 'it';
}

// Inizializza al caricamento
document.addEventListener('DOMContentLoaded', () => {
  const lang = rilevaLingua();
  applyLang(lang);
});
